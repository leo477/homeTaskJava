package com.itea.dimka.lection12.homeTask;

public class Product{
    private int price;
    private int age;

    public Product(int price, int age) {
        this.price = price;
        this.age = age;
    }

    public int getPrice() {
        return price;
    }

    public int getAge() {
        return age;
    }
}
